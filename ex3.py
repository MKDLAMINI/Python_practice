# Numbers and Math

# we use 'print' to output the statement/string
print("I will now count my chickens:")

# we print out the string alongside the math operation, the math operation is not highlighted in double quotes because it has to output the answer

print("Hens", 25 + 30 / 6)
print("Roosters", 100 - 25 * 3 % 4)

print("Now I will count the eggs:")

print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)

# here we ask the intepreter to output whether the question is true/false based on th answer of math operation
print("Is it true that 3 + 2 < 5 - 7?")

# we print out the answer 'true/false' 
print(3 + 2 < 5 - 7)

print("What is 3 + 2?", 3 + 2)
print("What is 5 - 7?", 5 - 7)

print("Oh, that's why it's False.")

print("How about some more.")

# the interpreter will output 'true/false' based on the results given 
print("Is it greater?", 5 > -2)
print("Is it greater or equal?", 5 >= -2)
print("Is it less or equal?", 5 <= -2)

